package com.kuk.sfgame.model;


public class GuildBonus {
    public int gold;
    public int xp;
    public int hp;
    public int dmg;
    public int questOfferNumber;

    public GuildBonus(int gold, int xp, int hp, int dmg, int questOfferNumber) {
        this.gold = gold;
        this.xp = xp;
        this.hp = hp;
        this.dmg = dmg;
        this.questOfferNumber = questOfferNumber;
    }
}


